    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        controlador = new Controlador();
        Corredor corredor = (Corredor) cboCorredores.getSelectedItem();
        float tiempoRealizado = Float.parseFloat(txtTiempoRealizado.getText());
        String club = txtClub.getText();
        String clima = txtClima.getText();
        Tiempo tiempo = new Tiempo(corredor, tiempoRealizado, club, clima);
        controlador.agregarTiempo(tiempo);
        actualizarLista();
        limpiarCampos();
        

    }//GEN-LAST:event_btnAgregarActionPerformed

