    private void cargarCombo() {
        controlador = new Controlador();
        ArrayList<Corredor> lista = controlador.obtenerCorredores();
        
        for (Corredor corredor : lista) {
            cboCorredores.addItem(corredor);
        }
        
    }
    
    private void actualizarLista() {
        controlador = new Controlador();
        ArrayList<DtoTiempos> lista = controlador.obtenerTiempos();
        DefaultListModel model = new DefaultListModel();
        lstTiempos.setModel(model);
        for (DtoTiempos dtoTiempos : lista) {
            model.addElement(dtoTiempos);
        }
        
    }
    
    private void limpiarCampos() {
        txtClima.setText("");
        txtClub.setText("");
        txtTiempoRealizado.setText("");
    }
